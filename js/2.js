function move(ele,dir,end){
		clearInterval(ele.timer);
		if(dir==="left"){
			ele.timer=setInterval(function(){
			ele.style.left=ele.offsetLeft-7+"px";
			if(ele.offsetLeft<=end){
				ele.style.left=end+"px";
				clearInterval(ele.timer)	
			}
		},30)
		}else{
			ele.timer=setInterval(function(){
			ele.style.left=ele.offsetLeft+7+"px";
			if(ele.offsetLeft>=end){
				ele.style.left=end+"px";
				clearInterval(ele.timer)
			}
		},30)
		}	
	}
